from shapely import wkb

# WKB Hex string
wkb_hex = "01030000000100000005000000C7BAB88D06BE63C0E0BE0E9C33A2F53FC7BAB88D06BE63C092CB7F48BF154E40B1506B9A77F4594092CB7F48BF154E40B1506B9A77F45940E0BE0E9C33A2F53FC7BAB88D06BE63C0E0BE0E9C33A2F53F"

# Convert hex to bytes
wkb_bytes = bytes.fromhex(wkb_hex)

# Convert WKB to geometry
geom = wkb.loads(wkb_bytes)

# Output WKT format
print(geom.wkt)
